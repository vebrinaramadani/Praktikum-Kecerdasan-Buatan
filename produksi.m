% --- Executes on button press in hitung.
function hitung_Callback(hObject, eventdata, handles)
% hObject    handle to hitung (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
minta1 = get(handles.edit1,'String');
minta2 = get(handles.edit2,'String');
minta3 = get(handles.edit3,'String');
minta4 = get(handles.edit4,'String');
minta5 = get(handles.edit5,'String');

sedia1 = get(handles.edit6,'String');
sedia2 = get(handles.edit7,'String');
sedia3 = get(handles.edit8,'String');
sedia4 = get(handles.edit9,'String');
sedia5 = get(handles.edit10,'String');

permintaan = (str2num(minta1)+ str2num(minta2)+ str2num(minta3)+ str2num(minta4)+ str2num(minta5))/5;
persediaan = (str2num(sedia1)+ str2num(sedia2)+ str2num(sedia3)+ str2num(sedia4)+ str2num(sedia5))/5;

ratarata = (50/100*permintaan)+(50/100*persediaan);

set(handles.permintaan,'String',permintaan);
set(handles.persediaan,'String',persediaan);

set(handles.ratahuruf,'String','Rata2 = ');
set(handles.rataangka,'String',ratarata);
set(handles.fuzzyhuruf,'String','Fuzzy ');

input = [permintaan persediaan];
a = readfis('produksikayu');
out = evalfis(input, a);
set(handles.fuzzyangka,'String', out);

if (out >=0 & out <=40)
    set(handles.text17,'String','Sedikit');
elseif (out >=41 & out <=60)
    set(handles.text17,'String','Sedang');  
else 
    (out >=61 & out <=100)
    set(handles.text17,'String','Banyak');  
end